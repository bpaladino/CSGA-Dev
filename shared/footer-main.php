                <div class="simple-text">The CSGA functions as an extension of the USGA and provides stewardship for amateur golf in Connecticut. Founded in 1899, it is the country’s oldest state golf association and conducts over 50 Championships, Qualifiers and One-Day Tournaments throughout the year, in addition to administering handicaps for over 40,000 members and 181 member clubs. As a 501(c)(6) non-profit organization, the CSGA supports a variety of golf organizations within Connecticut, including the Connecticut Women’s Golf Association, Southern New England Women’s Golf Association, The First Tee, the Connecticut PGA, and the CSGA Scholarship Fund in honor of Widdy Neale. </div>
